void load_pic(uint8_t* pic)
{
	int cnt = 0;
	for (int j=0; j<2;j++)
	{
		cnt = (j == 0) ? 0 : 64;
		for(int i =0; i<8;i++)
		{
			SetAddr(0, j);
			SetPage(i,j);
			for(int k=0;k<64;k++)
			{
				Send_Data(pic[cnt],j);
				cnt++;
			}
		cnt = cnt + 64;
		}
	}
	cnt = 0;
}	

